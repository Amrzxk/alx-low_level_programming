#include "3-calc.h"

/**
 * op_add - adds 2 integers
 *
 * @a: first num
 * @b: second num
 *
 * Return: the sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts 2 integers
 *
 * @a: num 1
 * @b: num 2
 *
 * Return: difference
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies 2 integers
 *
 * @a: num 1
 * @b: num 2
 *
 * Return: the product
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides 2 integers
 *
 * @a: num 1
 * @b: num 2
 *
 * Return: Division of nums
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modules of 2 integers
 *
 * @a: num 1
 * @b: num 2
 *
 * Return: modules of nums
*/

int op_mod(int a, int b)
{
	return (a % b);
}
